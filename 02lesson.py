def add(a,b):
    """ add two numbers together and return the output
    A is first number
    B is second number"""

    # add them together
    output = a+b

    # return value
    return output

def subtract(a,b):
    """Subtract two numbers from each other and return the output
    A is first number
    B is second number"""

    # subtract them from each other
    output = a-b

    # return value
    return output

def divide(a,b):
    """Divide two numbers and return the quiotient
    A is first number
    B is second number"""

    # divide them
    output = a/b

    # return value
    return output
